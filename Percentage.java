public class Percentage{
    public static void main(String[] args) {
        double s1 = 45;
        double s2 = 55;
        double s3 = 35;
        double s4 = 60;
        double sum = s1 + s2 + s3 + s4;
        System.out.println(sum);
        System.out.println("Percentage: "+(sum/240*100));
        
    } 
}